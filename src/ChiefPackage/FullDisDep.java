    private void UpdateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateBtnActionPerformed
        // TODO add your handling code here:
       JFrame f = new JFrame();  
         int a = JOptionPane.showConfirmDialog(f, "Are you sure you want to update?","Select an option",JOptionPane.YES_NO_OPTION); 
         if(a == JOptionPane.YES_OPTION)
         {  
            String s = String.valueOf(CIDBox.getSelectedItem());
            int dID = Integer.valueOf(IDTxt.getText()); 
            int newID = Integer.valueOf(s);
           
            chief.update_Captain(dID, oldID, newID);
            LoginPackage.LoginForm.load();    
           
            f.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
            this.setLayout(b);
            this.add(dep);
            
            IDLbl.setVisible(false);
            IDTxt.setVisible(false);
            NameLbl.setVisible(false);
            NameTxt.setVisible(false);
            CIDLbl.setVisible(false);
            CIDBox.setVisible(false);
            CNameLbl.setVisible(false);
            CNameTxt.setVisible(false);
            DateLbl.setVisible(false);
            DateTxt.setVisible(false);
            NumCLbl.setVisible(false);
            NumCTxt.setVisible(false);
            NumOLbl.setVisible(false);
            NumOTxt.setVisible(false);
            CaseLbl.setVisible(false);
            OfficerLbl.setVisible(false);
            OfficerList.setVisible(false);
            CaseList.setVisible(false);
            DisDep.f.setVisible(false);
            
            DepChief d = new DepChief();
            ChiefForm.BgPanel.removeAll();
            ChiefForm.BgPanel.revalidate();
            ChiefForm.BgPanel.repaint();
            ChiefForm.BgPanel.setLayout(b);
            ChiefForm.BgPanel.add(d);
            d.setVisible(true);
         }
    }//GEN-LAST:event_UpdateBtnActionPerformed

