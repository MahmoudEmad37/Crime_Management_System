    private void CIDBoxItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_CIDBoxItemStateChanged
        int CapID = Integer.valueOf(CIDBox.getSelectedItem().toString());
        for (int i =0 ;i< OfficersList.size(); i++)
        {
            if(OfficersList.get(i).getID() == CapID)
            {
                CNameTxt.setText(OfficersList.get(i).getName());
            }
        }
        for (int i =0 ;i< CaptainList.size(); i++)
        {
            if(CaptainList.get(i).getID() == CapID)
            {
                CNameTxt.setText(CaptainList.get(i).getName());
            }
        }
    }//GEN-LAST:event_CIDBoxItemStateChanged


